    pub fn from_scancode(scancode: Scancode) ?Keycode {
        return switch (sys.SDL_GetKeyFromScancode(@enumToInt(scancode))) {
            sys.SDLK_UNKNOWN => null,
            else => |keycode_id| Keycode.from_i32(keycode_id),
        };
    }

    pub fn from_name(name_: string) ?Keycode {
        return switch (sys.SDL_GetKeyFromName(name_.ptr)) {
            sys.SDLK_UNKNOWN => null,
            else => |keycode_id| Keycode.from_i32(keycode_id),

        };
    }

    pub fn name(self: Keycode, allocator: std.mem.Allocator) OwnedStr {
        const buf = sys.SDL_GetKeyName(@enumToInt(self));
        const as_slice: [:0]const u8 = std.mem.span(buf);
        return OwnedStr.from(allocator, as_slice);
    }
